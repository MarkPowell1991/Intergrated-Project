    private void validateForm() {
        Color isValidColor = new Color(224, 255, 224);
        Color isInvalidColor = new Color(255, 238, 238);
        
        if (txtForename.getBackground().equals(isValidColor)
                && txtSurname.getBackground().equals(isValidColor)
                && txtEmailAddress.getBackground().equals(isValidColor)
                && (txtMobileNo.getBackground().equals(isValidColor)
                || !txtMobileNo.getBackground().equals(isInvalidColor))
                && txtAddressLine1.getBackground().equals(isValidColor)
                && !txtAddressLine2.getBackground().equals(isInvalidColor)
                && txtPostcode.getBackground().equals(isValidColor)
                && txtPassword.getBackground().equals(isValidColor)
                && txtPasswordAgain.getBackground().equals(isValidColor)
                ) {
            addUser();
            dispose();
        } else {
            txtForename.requestFocus();
            txtSurname.requestFocus();
            txtEmailAddress.requestFocus();
            txtMobileNo.requestFocus();
            txtAddressLine1.requestFocus();
            txtAddressLine2.requestFocus();
            txtPostcode.requestFocus();
            txtPassword.requestFocus();
            txtPasswordAgain.requestFocus();
        }
    }
    
