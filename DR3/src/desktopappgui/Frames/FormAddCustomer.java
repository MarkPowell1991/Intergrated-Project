    private void addUser() {
        Customer p = new Customer();
        Address a;
        p.setGivenName(txtForename.getText());
        p.setFamilyName(txtSurname.getText());
        p.setEmail(txtEmailAddress.getText());
        p.setMobileNumber(txtMobileNo.getText());
        p.setDateOfBirth(jdcDOB.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
        a = new Address(txtAddressLine1.getText(), txtAddressLine2.getText(), txtTownCity.getText(), txtPostcode.getText());
        p.setAddress(a);
        p.setPassword(new String(txtPassword.getPassword()));
        peopleList.addNewCustomer(p);
    }      
    
